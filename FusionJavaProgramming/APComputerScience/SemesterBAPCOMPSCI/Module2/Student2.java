public class Student2 {
    
    private String name;
    private int id;

    public Student2(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }

    public void setName(String _name) {
        name = _name;
    }

    public void setId(int _id) {
        id = _id;
    }
}